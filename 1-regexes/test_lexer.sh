#!/bin/bash

# Try to find a portable way of getting rid of
# any stray carriage returns
if which dos2unix ; then
    DOS2UNIX="dos2unix"
elif which fromdos ; then
    DOS2UNIX="fromdos"
else
    case "$(uname -s)" in
    Darwin|Linux)
    DOS2UNIX="sed -e s/\r//g"
    ;;

    *)
        # This works on a GNU version of sed. I think this
        # will work in OSX as well, but don't have a machine
        # on which to test that. From reading the OSX docs,
        # it looks compatible.
        # The code \x0D is the ASCII code of carriage-return,
        # so it the regex should delete any CRs at the end of
        # a line (or anywhere in a line)
      DOS2UNIX="cat"
	# Tested for combinations of:
	# - Ubuntu
	# - Cygwin
	# and inputs formats:
	# - LF
	# - CRLF
    ;;
    esac
fi

echo "========================================"
echo " Cleaning the temporaries and outputs"
make clean
echo " Force building histogram"
make histogram -B
if [[ "$?" -ne 0 ]]; then
    echo "Build failed.";
fi
echo ""
mkdir -p test/out

echo "========================================="

PASSED=0
CHECKED=0

for i in test/in/*.txt; do
    echo "==========================="
    echo ""
    echo "Input file : ${i}"
    BASENAME=$(basename $i .txt);
    cat $i | ${DOS2UNIX} | ./histogram  > test/out/$BASENAME.stdout.txt  2> test/out/$BASENAME.stderr.txt

    diff <(cat test/ref/$BASENAME.stdout.txt | ${DOS2UNIX}) <(cat test/out/$BASENAME.stdout.txt) > test/out/$BASENAME.diff.txt
    if [[ "$?" -ne "0" ]]; then
        echo -e "\nERROR"
    else
        PASSED=$(( ${PASSED}+1 ));
    fi
    CHECKED=$(( ${CHECKED}+1 ));
done


echo "########################################"
echo "Passed ${PASSED} out of ${CHECKED}".
echo ""

case "$(uname -s)" in
    Darwin)
    echo ""
    echo "Warning: This appears not to be a Linux environment"
    echo "         Make sure you do a final run on a lab machine or an Ubuntu VM"
    exit 0
    ;;
esac

RELEASE=$(lsb_release -d)
if [[ $? -ne 0 ]]; then
    echo ""
    echo "Warning: This appears not to be a Linux environment"
    echo "         Make sure you do a final run on a lab machine or an Ubuntu VM"
else
    grep -q "Ubuntu 16.04" <(echo $RELEASE)
    FOUND=$?

    if [[ $FOUND -ne 0 ]]; then
        echo ""
        echo "Warning: This appears not to be the target environment"
        echo "         Make sure you do a final run on a lab machine or an Ubuntu VM"
    fi
fi
